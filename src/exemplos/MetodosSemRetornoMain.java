package exemplos;

public class MetodosSemRetornoMain {
    public static void main(String[] args) {
        marvel();
        capAmerica();
        dareDevil();
        venom();
        ironMan();
    }

    public static void marvel(){
        System.out.println("marvel");
    }

    public static void capAmerica(){
        System.out.println("cap America");
    }
    public static void dareDevil(){
        System.out.println("Dare Devil");
    }
    public static void venom(){
        System.out.println("Venom");
    }
    public static void ironMan(){
        System.out.println("Iron Man");
    }
}
